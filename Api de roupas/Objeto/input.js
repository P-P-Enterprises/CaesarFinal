  function filterByCategory(category, items) {
    category = category.toLowerCase();
    
    const filteredItems = items.filter(item => {
      const itemCategory = item.category.toLowerCase();
      return itemCategory === category;
    });
  
    const productsContainer = document.getElementById('products-container');
    productsContainer.innerHTML = '';
  
    filteredItems.forEach(item => {
      const productDiv = document.createElement('div');
      const productName = document.createElement('h3');
      const productImage = document.createElement('img');
      const productDescription = document.createElement('p');
  
      productName.textContent = item.name;
      productImage.src = `caminho/para/as/imagens/${item.category}/${item.id}.jpg`;
      productDescription.textContent = `Descrição: Alguma descrição do ${item.name}.`;
  
      productDiv.classList.add('headproduct');
      productName.classList.add('vlrfnl');
      productImage.classList.add('img-box');
      productDescription.classList.add('vlrinc');
  
      productDiv.appendChild(productName);
      productDiv.appendChild(productImage);
      productDiv.appendChild(productDescription);
  
      productsContainer.appendChild(productDiv);
    });
  }

  
function setupClickListeners() {
  document.getElementById('streetwearBtn').addEventListener('click', () => filterByCategory('streetwear',items));
  document.getElementById('casualBtn').addEventListener('click', () => filterByCategory('camisas casual', items));
  document.getElementById('socialBtn').addEventListener('click', () => filterByCategory('camisas social',items));
  document.getElementById('bonesBtn').addEventListener('click', () => filterByCategory('bonés', items));
  document.getElementById('tenisBtn').addEventListener('click', () => filterByCategory('tênis', items));
  document.getElementById('correntesBtn').addEventListener('click', () => filterByCategory('correntes', items));
  document.getElementById('relogiosBtn').addEventListener('click', () => filterByCategory('relógios', items));
}

// Chamada para configurar os listeners de clique
